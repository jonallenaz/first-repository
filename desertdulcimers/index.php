<h3>We lost a dulcimer treasure on June 1st, 2015 when Jean Ritchie
passed away at the age of 92.&nbsp; Stephen Seifert posted a wonderful
tribute to her in the form of a letter she wrote him some years ago and
I wanted to share that here.</h3>
<p style="margin: 6px 0px; display: block; color: rgb(20, 24, 35); font-family: helvetica,arial,'lucida grande',sans-serif; font-size: 14px; font-style: normal; font-variant: normal; font-weight: normal; letter-spacing: normal; line-height: 19.32px; text-align: left; text-indent: 0px; text-transform: none; white-space: normal; widows: 1; word-spacing: 0px; background-color: rgb(255, 255, 255);">"Yes,
I get to feeling that dulcimer playing has far outgrown me and left my
sort of playing behind. That's all OK, as long as my "cog" in that
wheel is still remembered by a few...there has to be a beginning
somewhere! I still like just playing alone, on a moonlit night, outside
with the lightnin bugs. Today's dulcimer scene has i<span class="text_exposed_show" style="display: inline;">ts
place, and great beauty and talent is there, but it gets a wee bit
frightening, when there are many players, and you feel the competetive
waves washing round you. That's all right; competition has its value,
as long as the soft lonesome magic can still be found and enjoyed by
each of those competitors when he/she is alone, in a quiet place with
the music.</span></p>
<div class="text_exposed_show" style="display: inline; color: rgb(20, 24, 35); font-family: helvetica,arial,'lucida grande',sans-serif; font-size: 14px; font-style: normal; font-variant: normal; font-weight: normal; letter-spacing: normal; line-height: 19.32px; text-align: left; text-indent: 0px; text-transform: none; white-space: normal; widows: 1; word-spacing: 0px; background-color: rgb(255, 255, 255);"><p style="margin: 0px 0px 6px;">I'll hush before I get too poetic- that's what even the thought of the dulcimer does to me!" - Jean Ritchie</p></div>

