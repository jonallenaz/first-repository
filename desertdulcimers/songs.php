<h3><u>FORKED DEER</u></h3>
<p>Forked Deer is the quintessential <em>Old Timey</em>
tune. Originally called Van Buren, it most likely originated as a
campaign tune during the presidential election of 1836.
&nbsp;&nbsp;Martin Van buren had been Attorney General of New
York,
U.S. Senator, Governor of New York, Secretary of State, and Vice
President during Andrew Jackson's second term. He became the 8th
President of the United States in 1837.&nbsp; His contemporaries
thought highly of him. Washington Irving said he was <em>one of
the gentlest and most amiable men I have ever met.</em> Andrew
Jackson said he was <em>a true man with no guile</em>.&nbsp;
Posterity has been less kind. He is mostly remembered for the dismal
human rights record of his administration, i.e., the forced relocation
of the Cherokee Indians, extending slavery into the Western Territories
and the Amistad incident. &nbsp;&nbsp;By the time of the Civil
War Van
Buren's association with Andrew Jackson and his politics of compromise
had made him very unpopular. The fiddle tune carrying his name needed a
new title.&nbsp; In the summer of 1862, Confederate General Braxton
Bragg, went on the offensive. He overran Tennessee and nearly captured
Kentucky. The rapidity of his advance overwhelmed the opposing Union
commanders, but his victory was brief. &nbsp;Defeated at
Perryville,
Kentucky. Bragg was forced to retreat across much of the ground he had
gained. &nbsp;He made a stand at Murfreesboro (a.k.a. Stone's
River) on
December 31, 1862 and January 2, 1863, but was forced to withdraw
again. This failed campaign became known in the North as Bragg's
Retreat. It provided the new name for the tune Van Buren. At some point
after the Civil War political correctness in the south led to renaming
the tune Forked Deer. It is one of the best examples of how good tunes
survive regardless of contemporary associations.</p>
