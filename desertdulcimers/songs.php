<h3><u>TURKEY</u><u> IN THE STRAW</u> </h3>
<p>Old
Time Music has many roots. One of the strangest is the minstrel show, a
peculiarly American entertainment. Developed in the 1840's it peaked in
popularity between 1850 and 1870, but survived into the 20th century
through the movies.&nbsp; Minstrel shows evolved from two types of
entertainment popular in America before 1830. The impersonation of
blacks by white actors between acts of plays and black street musicians
who performed with banjo accompaniment. The father of American
minstrelsy was Thomas Dartmouth Daddy Rice, who between 1828 and 1831
developed a song-and-dance routine in which he impersonated an old,
crippled black slave, called Jim Crow.&nbsp; Dan Emmett's Virginia
Minstrels, were one of the earliest troups. They first
performed&nbsp;
at New York's Bowery amphitheatre in 1843. The early minstrel shows
parodied popular topics using stock blackface characters such as Jim
Crow, an ignorant country bumpkin, and Zip Coon (derived from the name
Scipio), a self absorbed city slicker. The shows were in two parts. In
part one the performers were arranged in a semicircle, with a leader
(the interlocutor) in the center and Mr. Tambo, who played the
tambourine, and Mr. Bones, who played the bones, at opposing ends. The
interlocutor, in whiteface, wore formal attire; the others, in
blackface wore gaudy coats and striped trousers. The program opened
with a chorus followed by a series of jokes between the interlocutor
and end men, interspersed with ballads, comic songs, and instrumental
numbers, on the banjo and fiddle.&nbsp; The second part, or olio,
consisted of a series of short skits that concluded with a hoedown or
walk-around in which every member did a specialty number. <a target="_blank" href="http://hetzler.homestead.com/files/Old_Zip_Coon.mid">Old
Zip Coon</a> (Dan Emmett),&nbsp;<a target="_blank" href="http://hetzler.homestead.com/files/Dixie.mid">Dixie</a>
(Dan Emmett), <a target="_blank" href="http://hetzler.homestead.com/files/Turkey_in_the_Straw.mid">Turkey
in the Straw</a> (Dan Emmett), and <a target="_blank" href="http://hetzler.homestead.com/files/Golden_Slippers.mid">Golden
Slippers</a>
(James Bland 1854-1911) are examples of walk-around tunes.&nbsp;
Minstrel shows exploited stereotypes, but helped blacks enter show
business after the Civil War. Ironically, early black minstrel troupes
corked their faces just like the white minstrel performers.&nbsp;
Unbelievable as it seems today, this was considered wholesome family
entertainment in the 1800s. White and black audiences viewed the
blatant racism of the songs and skits as acceptable.</p>
