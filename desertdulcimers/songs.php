<h3><u>OLD YELLOW DOG</u></h3>
<p>The
whole title to this old time tune is Old Yellow Dog Came Trotting
Through the Meeting House. This tune is one that has many variants,
both in its name, and its melody. It is related to a tune called Old
Blind Dog, a tune popularized by east Tennessee fiddler Charlie Acuff.
The children's song The Old Grey Mare, She Ain't What She Used To Be is
also a descendant/variant of this tune.</p>
